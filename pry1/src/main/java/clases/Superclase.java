
package clases;
public class Superclase {
    
    private double m;
    private double c;
    private double v;

    public Superclase(double m, double c, double v) {
        this.m = m;
        this.c = c;
        this.v = v;
    }

    public Superclase() {
    }

    public double getM() {
        return m;
    }

    public void setM(double m) {
        this.m = m;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    public double getV() {
        return v;
    }

    public void setV(double v) {
        this.v = v;
    }
    
    
    
}
